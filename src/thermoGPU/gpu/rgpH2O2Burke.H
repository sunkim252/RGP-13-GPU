/*---------------------------------------------------------------------------*\
  Burke, Chaos, Ju, Dryer & Klippenstein (2012) H2/O2 메커니즘
  "Comprehensive H2/O2 kinetic model for high-pressure combustion",
  Int. J. Chem. Kinet. 44:444-474.

  9종 (H2 O2 H O OH HO2 H2O2 H2O N2-inert), 가역 19단계(중복 2쌍 포함).
  Arrhenius는 CHEMKIN 단위(cm3, mol, s, cal/mol)로 표기하고
  rgpBuildBurke2012()가 SI-kmol로 변환한다:
    A_SI = A_ck * (1e-3)^(order-1),  Ta = Ea/1.9872 [K]
  JANAF 계수는 OF-13 GRI 튜토리얼 thermo(무차원 NASA7)에서 그대로 추출.

  ⚠ 반응 파라미터는 원논문 대조 검증 전 상태 — 점화지연 검증으로 1차
  확인하되, 프로덕션 사용 전 CHEMKIN 원본 파싱으로 교체할 것.
\*---------------------------------------------------------------------------*/

#ifndef rgpH2O2Burke_H
#define rgpH2O2Burke_H

#include "rgpChemTypes.H"
#include <string.h>
#include <math.h>

/* 종 인덱스 */
enum { iH2 = 0, iO2, iH, iO, iOH, iHO2, iH2O2, iH2O, iN2, nSp9 };

inline void rgpBuildBurke2012(rgpChemMech& m)
{
    memset(&m, 0, sizeof(m));
    m.nSpecies = nSp9;
    m.RR = 8314.462618;       /* J/(kmol K) — Foam 상수와 일치 */
    m.pRef = 101325.0;

    const double Wv[nSp9] =
        {2.01594, 31.9988, 1.00797, 15.9994, 17.00737,
         33.00677, 34.01474, 18.01534, 28.0134};
    for (int s = 0; s < nSp9; s++) { m.W[s] = Wv[s]; m.Tcommon[s] = 1000.0; }

    /* GRI NASA7 (무차원), OF-13 SandiaD 튜토리얼 thermo에서 추출 */
    const double hi[nSp9][7] = {
    {3.3372792, -4.94024731e-05, 4.99456778e-07, -1.79566394e-10, 2.00255376e-14, -950.158922, -3.20502331},   // H2
    {3.28253784, 0.00148308754, -7.57966669e-07, 2.09470555e-10, -2.16717794e-14, -1088.45772, 5.45323129},    // O2
    {2.50000001, -2.30842973e-11, 1.61561948e-14, -4.73515235e-18, 4.98197357e-22, 25473.6599, -0.446682914},  // H
    {2.56942078, -8.59741137e-05, 4.19484589e-08, -1.00177799e-11, 1.22833691e-15, 29217.5791, 4.78433864},    // O
    {3.09288767, 0.000548429716, 1.26505228e-07, -8.79461556e-11, 1.17412376e-14, 3858.657, 4.4766961},        // OH
    {4.0172109, 0.00223982013, -6.3365815e-07, 1.1424637e-10, -1.07908535e-14, 111.856713, 3.78510215},        // HO2
    {4.16500285, 0.00490831694, -1.90139225e-06, 3.71185986e-10, -2.87908305e-14, -17861.7877, 2.91615662},    // H2O2
    {3.03399249, 0.00217691804, -1.64072518e-07, -9.7041987e-11, 1.68200992e-14, -30004.2971, 4.9667701},      // H2O
    {2.92664, 0.0014879768, -5.68476e-07, 1.0097038e-10, -6.753351e-15, -922.7977, 5.980528}};                 // N2
    const double lo[nSp9][7] = {
    {2.34433112, 0.00798052075, -1.9478151e-05, 2.01572094e-08, -7.37611761e-12, -917.935173, 0.683010238},
    {3.78245636, -0.00299673416, 9.84730201e-06, -9.68129509e-09, 3.24372837e-12, -1063.94356, 3.65767573},
    {2.5, 7.05332819e-13, -1.99591964e-15, 2.30081632e-18, -9.27732332e-22, 25473.6599, -0.446682853},
    {3.1682671, -0.00327931884, 6.64306396e-06, -6.12806624e-09, 2.11265971e-12, 29122.2592, 2.05193346},
    {3.99201543, -0.00240131752, 4.61793841e-06, -3.88113333e-09, 1.3641147e-12, 3615.08056, -0.103925458},
    {4.30179801, -0.00474912051, 2.11582891e-05, -2.42763894e-08, 9.29225124e-12, 294.80804, 3.71666245},
    {4.27611269, -0.000542822417, 1.67335701e-05, -2.15770813e-08, 8.62454363e-12, -17702.5821, 3.43505074},
    {4.19864056, -0.0020364341, 6.52040211e-06, -5.48797062e-09, 1.77197817e-12, -30293.7267, -0.849032208},
    {3.298677, 0.0014082404, -3.963222e-06, 5.641515e-09, -2.444854e-12, -1020.8999, 3.950372}};
    memcpy(m.janafHigh, hi, sizeof(hi));
    memcpy(m.janafLow, lo, sizeof(lo));

    /* 반응 채우기 헬퍼 (CHEMKIN 단위 입력) */
    int R = 0;
    const double calK = 1.9872;                 /* cal/mol/K */
    auto addR = [&](double A, double b, double Ea, int order,
                    int l0,int nl0, int l1,int nl1,
                    int r0,int nr0, int r1,int nr1, int tb) -> int
    {
        const int r = R++;
        m.A[r] = A*pow(1e-3, order - 1);        /* cm3/mol → m3/kmol */
        m.beta[r] = b;
        m.Ta[r] = Ea/calK;
        m.reversible[r] = 1;
        if (l0 >= 0) m.nuL[r][l0] += nl0;
        if (l1 >= 0) m.nuL[r][l1] += nl1;
        if (r0 >= 0) m.nuR[r][r0] += nr0;
        if (r1 >= 0) m.nuR[r][r1] += nr1;
        m.tbType[r] = tb;
        if (tb != RGP_TB_NONE)
            for (int s = 0; s < nSp9; s++) m.eff[r][s] = 1.0;
        return r;
    };

    /* ── H2/O2 연쇄 ── */
    addR(1.04e14, 0.0, 15286., 2, iH,1,iO2,1,  iO,1,iOH,1, RGP_TB_NONE);   // H+O2=O+OH
    addR(3.818e12,0.0, 7948.,  2, iO,1,iH2,1,  iH,1,iOH,1, RGP_TB_NONE);   // O+H2=H+OH (dup1)
    addR(8.792e14,0.0, 19170., 2, iO,1,iH2,1,  iH,1,iOH,1, RGP_TB_NONE);   // O+H2=H+OH (dup2)
    addR(2.16e8, 1.51, 3430.,  2, iH2,1,iOH,1, iH2O,1,iH,1, RGP_TB_NONE);  // H2+OH=H2O+H
    addR(3.34e4, 2.42, -1930., 2, iOH,2,0,0,   iO,1,iH2O,1, RGP_TB_NONE);  // OH+OH=O+H2O

    /* ── 3체 재결합 ── */
    int r;
    r = addR(4.577e19,-1.40,104380.,2, iH2,1,-1,0, iH,2,-1,0, RGP_TB_M);   // H2+M=H+H+M
    m.eff[r][iH2]=2.5; m.eff[r][iH2O]=12.0;
    r = addR(6.165e15,-0.50,0.,     3, iO,2,-1,0,  iO2,1,-1,0, RGP_TB_M);  // O+O+M=O2+M
    m.eff[r][iH2]=2.5; m.eff[r][iH2O]=12.0;
    r = addR(4.714e18,-1.00,0.,     3, iO,1,iH,1,  iOH,1,-1,0, RGP_TB_M);  // O+H+M=OH+M
    m.eff[r][iH2]=2.5; m.eff[r][iH2O]=12.0;
    r = addR(3.5e22, -2.00,0.,      3, iH,1,iOH,1, iH2O,1,-1,0, RGP_TB_M); // H+OH+M=H2O+M
    m.eff[r][iH2]=0.73; m.eff[r][iH2O]=3.65;

    /* ── HO2 생성 (폴오프, Troe) ── */
    r = addR(4.65084e12,0.44,0.,    2, iH,1,iO2,1, iHO2,1,-1,0, RGP_TB_TROE); // H+O2(+M)=HO2(+M)
    m.A0[r]=6.366e20*pow(1e-3,2);   /* k0: 3체 차수 = order+1 */
    m.beta0[r]=-1.72; m.Ta0[r]=524.8/calK;
    m.troe[r][0]=0.5; m.troe[r][1]=1e-30; m.troe[r][2]=1e30; m.troe[r][3]=0.;
    m.eff[r][iH2]=2.0; m.eff[r][iH2O]=14.0; m.eff[r][iO2]=0.78;

    /* ── HO2 소비 ── */
    addR(2.750e6, 2.09, -1451., 2, iHO2,1,iH,1, iH2,1,iO2,1, RGP_TB_NONE);
    addR(7.079e13,0.0,  295.,   2, iHO2,1,iH,1, iOH,2,-1,0,  RGP_TB_NONE);
    addR(2.850e10,1.00, -723.93,2, iHO2,1,iO,1, iO2,1,iOH,1, RGP_TB_NONE);
    addR(2.890e13,0.0,  -497.,  2, iHO2,1,iOH,1,iH2O,1,iO2,1,RGP_TB_NONE);

    /* ── H2O2 경로 ── */
    addR(4.200e14,0.0, 11982.,  2, iHO2,2,-1,0, iH2O2,1,iO2,1, RGP_TB_NONE); // dup1
    addR(1.300e11,0.0, -1629.3, 2, iHO2,2,-1,0, iH2O2,1,iO2,1, RGP_TB_NONE); // dup2
    r = addR(2.00e12, 0.90, 48749., 1, iH2O2,1,-1,0, iOH,2,-1,0, RGP_TB_TROE); // H2O2(+M)=2OH(+M)
    m.A0[r]=2.49e24*pow(1e-3,1);
    m.beta0[r]=-2.30; m.Ta0[r]=48749./calK;
    m.troe[r][0]=0.43; m.troe[r][1]=1e-30; m.troe[r][2]=1e30; m.troe[r][3]=0.;
    m.eff[r][iH2O]=7.5; m.eff[r][iH2O2]=7.7; m.eff[r][iH2]=3.7;
    m.eff[r][iO2]=1.2; m.eff[r][iN2]=1.5;
    addR(2.410e13,0.0, 3970., 2, iH2O2,1,iH,1,  iH2O,1,iOH,1, RGP_TB_NONE);
    addR(4.820e13,0.0, 7950., 2, iH2O2,1,iH,1,  iHO2,1,iH2,1, RGP_TB_NONE);
    addR(9.550e6, 2.00,3970., 2, iH2O2,1,iO,1,  iOH,1,iHO2,1, RGP_TB_NONE);
    addR(1.740e12,0.0, 318.,  2, iH2O2,1,iOH,1, iHO2,1,iH2O,1,RGP_TB_NONE); // dup1
    addR(7.590e13,0.0, 7270., 2, iH2O2,1,iOH,1, iHO2,1,iH2O,1,RGP_TB_NONE); // dup2

    m.nReactions = R;
}

#endif

